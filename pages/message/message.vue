<template>
	<view>
		<button type="primary" @click="chooseImg">上传图片</button>
		<image v-for="item in imgArr" :src="item" @click="previewImg(item)"></image>
		<!-- #ifdef H5-->
		<view>我希望只在h5页面中出现</view>
		<!-- #endif -->
		<!-- #ifdef MP-WEIXIN -->
		<view>我希望只在小程序页面中出现</view>
		<!-- #endif -->
	</view>
</template>

<script>
	export default{
		onLoad(options) {
			console.log('信息页面卸载了',options)
		},
		data() {
			return{
				imgArr: []
			}
		},
		methods:{
			chooseImg(){
				uni.chooseImage({
					count: 5,
					success: res=>{
						this.imgArr = res.tempFilePaths
					}
				})
			},
			previewImg(current) {
				console.log(current)
				uni.previewImage({
					current,
					urls: this.imgArr,
					indicator: 'number'
				})
			}
		},
		onLoad(){
			// #ifdef H5
			console.log('我希望在h5中打印')
			// #endif
			// #ifdef MP-WEIXIN
			console.log('我希望在微信小程序中打印')
			// #endif
		}
	}
</script>

